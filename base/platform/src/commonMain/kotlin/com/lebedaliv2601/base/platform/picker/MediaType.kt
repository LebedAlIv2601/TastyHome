package com.lebedaliv2601.base.platform.picker

/**
 * Типы медиа для выбора через FilePicker
 */
enum class MediaType {
    /**
     * Только изображения (JPG, PNG, WEBP и т.д.)
     */
    IMAGE,
    
    /**
     * Только видео (MP4, MOV и т.д.)
     */
    VIDEO,
    
    /**
     * Изображения и видео
     */
    ALL
}
