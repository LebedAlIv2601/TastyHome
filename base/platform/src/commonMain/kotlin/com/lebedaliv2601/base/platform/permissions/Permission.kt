package com.lebedaliv2601.base.platform.permissions

/**
 * Типы разрешений для запроса у пользователя
 */
enum class Permission {
    /**
     * Доступ к камере
     */
    CAMERA,
    
    /**
     * Доступ к геолокации
     */
    LOCATION,
    
    /**
     * Доступ к хранилищу файлов
     */
    STORAGE,
    
    /**
     * Доступ к микрофону
     */
    MICROPHONE,
    
    /**
     * Доступ к галерее/фото
     */
    MEDIA,

    /**
     * Доступ к уведомлениям (отображение в системе)
     */
    NOTIFICATION
}

/**
 * Статус разрешения
 */
enum class PermissionStatus {
    /**
     * Разрешение предоставлено
     */
    GRANTED,
    
    /**
     * Разрешение отклонено
     */
    DENIED,
    
    /**
     * Разрешение отклонено навсегда (пользователь выбрал "больше не спрашивать")
     */
    DENIED_FOREVER,
    
    /**
     * Статус не определен (еще не запрашивали)
     */
    NOT_DETERMINED
}
